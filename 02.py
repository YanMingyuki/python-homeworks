a = int(input())
sum1 = 0
sum2 =0
if a<=120 :
    sum1 = a*2.10
    sum2 = a*2.10
elif 120<a<=330:
    sum1 = 120*2.10+(a-120)*3.02
    sum2 = 120*2.10+(a-120)*2.68
elif 330<a<=500:
    sum1 = 120*2.10+(330-120)*3.02+(a-330)*4.39
    sum2 = 120*2.10+(330-120)*2.68+(a-330)*3.61
elif 500<a<=700:
    sum1 = 120*2.10+(330-120)*3.02+(500-330)*4.39+(a-500)*4.97
    sum2 = 120*2.10+(330-120)*2.68+(500-330)*3.61+(a-500)*4.01
elif 700<a:
    sum1 = 120*2.10+(330-120)*3.02+(500-330)*4.39+(700-500)*4.97+(a-700)*5.63
    sum2 = 120*2.10+(330-120)*2.68+(500-330)*3.61+(700-500)*4.01+(a-700)*4.50
print("Summer months:",round(sum1,2))
print("Non-Summer months:",round(sum2,2))